#Python 3 has three numeric data types:
#   - Int
#   - Float
#   - Decimal
#   - Complex

a = 12
b = 3

print(a + b)    # 15
print(a - b)    # 9
print(a * b)    # 36
print(a / b)    # 4.0
print(a // b)   # 4  Integer division, round down towards minus infinity
print(a % b)    # 0 Modulus: The remainder after integer division

for i in range(1, 4):   # 1 (inclusive) to 4 (exclusive)
    print(i)